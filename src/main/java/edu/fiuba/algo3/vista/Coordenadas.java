package edu.fiuba.algo3.vista;

public class Coordenadas {
    private final double x;
    private final double y;

    public Coordenadas(int x, int y){
        this.x = x;
        this.y = y;
    }
    public double getX(){
        return this.x;
    }

    public double getY() {
        return this.y;
    }
}
